def main():
    n = read_int()
    arr = read_int_list()
    total_sum = sum(arr)
    if total_sum % 3 != 0:
        print("0")
        return
    target = total_sum // 3
    prefix_sum = 0
    res = 0
    all_possible_target_j_to_n = [0] * (n + 1)
    suffix_sum = 0
    for j in range(n)[::-1]:
        suffix_sum += arr[j]
        all_possible_target_j_to_n[j] = all_possible_target_j_to_n[j + 1] + int(suffix_sum == target)
    for i in range(n - 2):
        prefix_sum += arr[i]
        if prefix_sum == target:
            res += all_possible_target_j_to_n[i + 2]
    print(res)


