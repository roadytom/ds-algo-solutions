def main():
    n, q = read_int_list()
    arr = read_int_list()
    used = [0] * n
    queries = []
    for _ in range(q):
        queries.append(read_int_list_0_indexed())
    for l, r in queries:
        used[l] += 1
        if r + 1 < n:
            used[r + 1] -= 1
    idx_to_used_count = []
    prefix_sum = 0
    for u in used:
        prefix_sum += u
        idx_to_used_count.append(prefix_sum)
    idx_to_used_count.sort(reverse=True)
    arr.sort(reverse=True)
    res = 0
    for c, num in zip(idx_to_used_count, arr):
        res += c * num
    print(res)


if __name__ == '__main__':
    main()
