def main():
    N, M = read_int_list()
    arr = read_int_list()
    sorted_set = SortedSet()
    for i in range(len(arr) + 1):
        sorted_set.add(i)
    left, right = 0, 0
    counter = Counter()
    ans = float("inf")
    while right < N:
        counter[arr[right]] += 1
        sorted_set.discard(arr[right])
        if right - left + 1 == M:
            ans = min(ans, sorted_set[0])
            counter[arr[left]] -= 1
            if counter[arr[left]] == 0:
                sorted_set.add(arr[left])
            left += 1
        right += 1
    bisect.bisect
    print(ans)


if __name__ == '__main__':
    main()
